    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
         if(Utb.getText().isEmpty() || Passwordtb.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Missing Information");
    } else {
        String username = Utb.getText();
        String password = Passwordtb.getText();
        String hashedPassword = hashPassword(password); // Hash the password

        String query = "SELECT * FROM mysql.usertbl WHERE UName=? AND UPassword=?";
        try {
            Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/mysql?zeroDateTimeBehavior=CONVERT_TO_NULL", "root", "root");
            java.sql.PreparedStatement pstmt = Con.prepareStatement(query);
            pstmt.setString(1, username);
            pstmt.setString(2, hashedPassword); // Use hashed password in the query
            ResultSet rs = pstmt.executeQuery();

            if(rs.next()) {
                // Successful login
                new HomeForm().setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Wrong Username or Password");
            }
        } catch(Exception e) {
            e.printStackTrace();
        }
    }
    }//GEN-LAST:event_jButton1MouseClicked
private String hashPassword(String password) {
        try {
            MessageDigest md = MessageDigest.getInstance("SHA-256");
            byte[] hashBytes = md.digest(password.getBytes());
            StringBuilder sb = new StringBuilder();
            for (byte b : hashBytes) {
                // Convert byte to hexadecimal representation
                sb.append(String.format("%02x", b));
            }
            return sb.toString();
        } catch (NoSuchAlgorithmException e) {
           // e.printStackTrace();
            return null;
        }
    }
